// Generate a static-require bundle entry for a MikeMcl-style self-contained
// test runner (decimal.js / big.js / bignumber.js).
//
// Those runners iterate a module list and load each with a *dynamic*
// require(PREFIX + name), which esbuild cannot resolve statically — so nothing
// gets bundled and the suite dies with "Module not found in bundle". We read
// the original entry, extract the module list, the require prefix, and the
// harness global (`T` / `test` / `Test`), then emit an equivalent entry that
// requires each module with a literal string so esbuild bundles them all.
//
// Usage: node gen-mikemcl-entry.js <orig-entry> <out-entry>

const fs = require("fs");

const [, , origEntry, outEntry] = process.argv;
if (!origEntry || !outEntry) {
  console.error("usage: gen-mikemcl-entry.js <orig-entry> <out-entry>");
  process.exit(1);
}

const src = fs.readFileSync(origEntry, "utf8");

// The module list is the array literal immediately before `.forEach`.
const arrMatch = src.match(/\[([\s\S]*?)\]\s*\.forEach/);
if (!arrMatch) {
  console.error("gen-mikemcl-entry: could not find [...].forEach module list");
  process.exit(1);
}
const names = (arrMatch[1].match(/['"]([^'"]+)['"]/g) || []).map((s) =>
  s.slice(1, -1)
);
if (names.length === 0) {
  console.error("gen-mikemcl-entry: module list is empty");
  process.exit(1);
}

// The dynamic require prefix, e.g. "./modules/" or "./methods/".
const prefixMatch = src.match(/require\(\s*['"]([^'"]+)['"]\s*\+/);
if (!prefixMatch) {
  console.error("gen-mikemcl-entry: could not find dynamic require prefix");
  process.exit(1);
}
const prefix = prefixMatch[1];

// The harness global that each module sets, read via `<G>.result[...]`.
const globalMatch = src.match(/(\w+)\.result\s*\[/);
if (!globalMatch) {
  console.error("gen-mikemcl-entry: could not find harness global (.result)");
  process.exit(1);
}
const g = globalMatch[1];

let out = "// Auto-generated by scripts/gen-mikemcl-entry.js — do not edit.\n";
out += "var __passed = 0, __total = 0;\n";
for (const name of names) {
  out +=
    `require(${JSON.stringify(prefix + name)}); ` +
    `__passed += ${g}.result[0]; __total += ${g}.result[1];\n`;
}
out +=
  "console.log('\\n In total, ' + __passed + ' of ' + __total + " +
  "' tests passed.\\n');\n";

fs.writeFileSync(outEntry, out);
console.log(
  `gen-mikemcl-entry: ${names.length} modules, prefix '${prefix}', global '${g}' -> ${outEntry}`
);
