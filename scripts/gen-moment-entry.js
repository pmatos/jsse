#!/usr/bin/env node
// Generate a static esbuild entry for Moment's QUnit suite.
//
// Upstream's node-qunit task discovers transpiled files through fs, and Moment
// lazily requires locale modules by name. The library harness has neither
// runtime filesystem discovery nor dynamic require resolution, so this entry
// imports every locale definition and test module at bundle time.

"use strict";

const fs = require("fs");
const path = require("path");

if (process.argv.length !== 3) {
  console.error("usage: node gen-moment-entry.js <output-entry>");
  process.exit(2);
}

const output = path.resolve(process.argv[2]);

function jsFiles(relativeDir) {
  const directory = path.resolve(relativeDir);
  const files = fs
    .readdirSync(directory, { withFileTypes: true })
    .filter((entry) => entry.isFile() && entry.name.endsWith(".js"))
    .map((entry) => path.join(directory, entry.name))
    .sort();
  if (files.length === 0) {
    throw new Error(`no JavaScript files found under ${relativeDir}`);
  }
  return files;
}

function importLine(file) {
  let relative = path
    .relative(path.dirname(output), file)
    .split(path.sep)
    .join("/");
  if (!relative.startsWith(".")) relative = "./" + relative;
  return `import ${JSON.stringify(relative)};`;
}

const localeSources = jsFiles("src/locale");
const momentTests = jsFiles("src/test/moment");
const localeTests = jsFiles("src/test/locale");
const imports = [...localeSources, ...momentTests, ...localeTests].map(importLine);

const entry = `// Generated by scripts/gen-moment-entry.js.
${imports.join("\n")}

// Capture the registered test count rather than using QUnit's assertion total.
// A failing test can stop before its remaining assertions, but it was still
// discovered and executed; this count therefore stays suitable for the Node
// cross-check while the normal QUnit summary keeps every failure visible.
let registeredTests = 0;
QUnit.config.autostart = false;
QUnit.config.sync = true;
QUnit.begin(function (details) {
  registeredTests = details.totalTests;
  console.log("Moment: " + registeredTests + " tests registered");
});
QUnit.done(function (details) {
  console.log(
    "Moment: " + registeredTests + " tests, " + details.total + " assertions"
  );
});
QUnit.start();
`;

fs.mkdirSync(path.dirname(output), { recursive: true });
fs.writeFileSync(output, entry);
console.log(
  `gen-moment-entry: imported ${localeSources.length} locales, ` +
    `${momentTests.length} core tests, and ${localeTests.length} locale tests`
);
