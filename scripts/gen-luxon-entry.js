#!/usr/bin/env node
// Generate a static esbuild entry for Luxon's Jest suite.
//
// Jest's CLI discovers files through fs and runs workers, neither of which is
// part of the library-test host floor. The shared in-process runner supplies
// test/describe; this generator installs Jest's published matcher core and
// statically imports every pinned *.test.js file.

"use strict";

const fs = require("fs");
const path = require("path");

if (process.argv.length !== 3) {
  console.error("usage: node gen-luxon-entry.js <output-entry>");
  process.exit(2);
}

const output = path.resolve(process.argv[2]);
const testRoot = path.resolve("test");
const expectSetup = path.join(path.dirname(output), "jsse-expect.js");

function walk(dir) {
  const files = [];
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const file = path.join(dir, entry.name);
    if (entry.isDirectory()) files.push(...walk(file));
    else if (entry.isFile() && entry.name.endsWith(".test.js")) files.push(file);
  }
  return files;
}

const testFiles = walk(testRoot).sort();
if (testFiles.length === 0) {
  throw new Error("no Luxon test files found under test/");
}

const setupSource = `// Generated by scripts/gen-luxon-entry.js.
import jestMatchers from "expect/build/matchers";
import {
  equals,
  iterableEquality,
  subsetEquality,
} from "@jest/expect-utils";
import * as matcherUtils from "jest-matcher-utils";

const utils = Object.assign({}, matcherUtils, {
  iterableEquality,
  subsetEquality,
});

function throwResult(received, expected) {
  if (typeof received !== "function") {
    return {
      pass: false,
      message: () => "Expected value to be a function",
    };
  }

  let thrown;
  try {
    received();
  } catch (error) {
    thrown = error;
  }

  let pass = thrown !== undefined;
  if (pass && expected !== undefined) {
    if (typeof expected === "function") {
      pass = thrown instanceof expected;
    } else if (typeof expected === "string") {
      const message =
        thrown && thrown.message !== undefined ? thrown.message : String(thrown);
      pass = String(message).includes(expected);
    } else if (expected && typeof expected.test === "function") {
      const message =
        thrown && thrown.message !== undefined ? thrown.message : String(thrown);
      pass = expected.test(String(message));
    } else {
      pass = equals(thrown, expected);
    }
  }

  return {
    pass,
    message: () =>
      pass
        ? "Expected function not to throw"
        : "Expected function to throw" +
          (expected === undefined ? "" : " " + String(expected)),
  };
}

function inlineSnapshotResult(received, expected) {
  let serialized;
  try {
    serialized = JSON.stringify(received);
  } catch (error) {
    serialized = String(received);
  }
  return {
    pass: serialized === expected,
    message: () =>
      "Expected inline snapshot " +
      String(expected) +
      ", received " +
      String(serialized),
  };
}

const matchers = Object.assign({}, jestMatchers, {
  toThrow: throwResult,
  toThrowError: throwResult,
  toMatchInlineSnapshot: inlineSnapshotResult,
});

function matcherSide(actual, isNot) {
  const side = {};
  for (const name of Object.keys(matchers)) {
    side[name] = function (...args) {
      const result = matchers[name].call(
        {
          customTesters: [],
          equals,
          expand: false,
          isNot,
          promise: "",
          utils,
        },
        actual,
        ...args
      );
      if (!result || typeof result.pass !== "boolean") {
        throw new Error("Invalid result from Jest matcher " + name);
      }
      if (result.pass === isNot) {
        throw new Error(
          typeof result.message === "function"
            ? result.message()
            : result.message || "Jest matcher " + name + " failed"
        );
      }
    };
  }
  return side;
}

globalThis.expect = function expect(actual) {
  const positive = matcherSide(actual, false);
  positive.not = matcherSide(actual, true);
  return positive;
};
`;

const entryImports = ['import "./jsse-expect.js";'];
for (const file of testFiles) {
  let relative = path.relative(path.dirname(output), file).split(path.sep).join("/");
  if (!relative.startsWith(".")) relative = "./" + relative;
  entryImports.push(`import ${JSON.stringify(relative)};`);
}

fs.writeFileSync(expectSetup, setupSource);
fs.writeFileSync(output, "// Generated by scripts/gen-luxon-entry.js.\n" + entryImports.join("\n") + "\n");
console.log(`gen-luxon-entry: imported ${testFiles.length} Luxon test files`);
