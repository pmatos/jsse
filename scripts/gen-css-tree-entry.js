#!/usr/bin/env node
// Generate a static, filesystem-free esbuild entry for css-tree's own Mocha
// suite (lib/__tests).
//
// The upstream suite is `mocha lib/__tests --require .../setup.js`: a
// directory-glob CLI run whose fixture-loading modules call
// fs.readdirSync/readFileSync/statSync directly. The library harness has
// neither Mocha's CLI nor a real filesystem, so this entry statically
// imports every top-level test file (Mocha's describe/it are ambient
// globals node-test-harness.js installs) and snapshots the read-only
// fixtures/ tree plus package.json into a manifest consumed by
// node-fs-module.js. helpers/setup.js is deliberately excluded: it only
// installs a poison-pill getter on Object.prototype guarding against
// non-own-property reads, and no test file depends on it being present
// (confirmed: it's the only file referencing __proto_pollute__) — the
// registered/passing counts are identical with or without it, so leaving it
// out removes a hazard (an enumerable throw-on-read Object.prototype getter)
// every shim and the harness would otherwise have to tolerate, for free.

"use strict";

const fs = require("fs");
const path = require("path");

if (process.argv.length !== 3) {
  console.error("usage: node gen-css-tree-entry.js <output-entry>");
  process.exit(2);
}

const output = path.resolve(process.argv[2]);
const outputDir = path.dirname(output);
const repoRoot = process.cwd();

function serialize(value) {
  // U+2028/U+2029 are valid JSON but unsafe in pre-JSON-superset JS parsers.
  return JSON.stringify(value)
    .replace(new RegExp(String.fromCharCode(0x2028), "g"), "\\u2028")
    .replace(new RegExp(String.fromCharCode(0x2029), "g"), "\\u2029");
}

// Snapshot a read-only directory tree (recursively) into {files, dirs},
// keyed by POSIX paths relative to the repo root — the same shape
// fs.readdirSync/readFileSync/statSync calls in the test suite address.
function snapshotDir(relDir, files, dirs) {
  const absDir = path.join(repoRoot, relDir);
  const names = fs.readdirSync(absDir).sort();
  dirs[relDir] = names;

  for (const name of names) {
    const relPath = relDir + "/" + name;
    const absPath = path.join(repoRoot, relPath);
    if (fs.statSync(absPath).isDirectory()) {
      snapshotDir(relPath, files, dirs);
    } else {
      files[relPath] = fs.readFileSync(absPath, "utf8");
    }
  }
}

const files = {};
const dirs = {};
snapshotDir("fixtures", files, dirs);
files["package.json"] = fs.readFileSync(path.join(repoRoot, "package.json"), "utf8");

const testDir = path.join(repoRoot, "lib", "__tests");
const testFiles = fs
  .readdirSync(testDir, { withFileTypes: true })
  .filter((entry) => entry.isFile() && entry.name.endsWith(".js") && entry.name !== "helpers")
  .map((entry) => entry.name)
  .sort();

if (testFiles.length === 0) {
  throw new Error("gen-css-tree-entry: no top-level test files found under lib/__tests");
}

const manifestPath = path.join(outputDir, "jsse-vfs-manifest.js");
const manifestSource = `// Generated by scripts/gen-css-tree-entry.js — do not edit.
// Sets the read-only virtual-fs manifest node-fs-module.js serves on jsse.
// Imported first by jsse-entry.js so it is fully evaluated (per ES module
// evaluation order for sibling imports) before any fixture-loading test
// file's own top-level fs.readdirSync/readFileSync calls run.
globalThis.__VFS_MANIFEST__ = { files: ${serialize(files)}, dirs: ${serialize(dirs)} };
`;

const entryImports = ["./jsse-vfs-manifest.js", ...testFiles.map((name) => "./" + name)]
  .map((specifier) => `import ${JSON.stringify(specifier)};`)
  .join("\n");

const entrySource = `// Generated by scripts/gen-css-tree-entry.js — do not edit.
// Mocha's describe/it/before/after/beforeEach/afterEach are ambient globals
// (node-test-harness.js installs them); the upstream test files reference
// them unmodified, same as running them under real Mocha's CLI.
${entryImports}
`;

fs.mkdirSync(outputDir, { recursive: true });
fs.writeFileSync(manifestPath, manifestSource);
fs.writeFileSync(output, entrySource);

console.log(
  `gen-css-tree-entry: imported ${testFiles.length} test files, ` +
    `snapshotted ${Object.keys(files).length} fixture files across ` +
    `${Object.keys(dirs).length} directories`
);
