#!/usr/bin/env node
'use strict';

// Generate a filesystem-free PrismJS language-test entry for the library
// harness. Run from a PrismJS checkout; the sole argument is the output path.

const fs = require('fs');
const path = require('path');

const root = process.cwd();
const output = process.argv[2];

if (!output) {
	console.error('usage: gen-prism-entry.js <output>');
	process.exit(2);
}

const components = require(path.join(root, 'components.json'));
const getLoader = require(path.join(root, 'dependencies'));
const languagesRoot = path.join(root, 'tests', 'languages');

/** @param {string} languageIdentifier */
function parseLanguageNames(languageIdentifier) {
	let mainLanguage = null;
	const languages = languageIdentifier.split('+').map(language => {
		if (language.includes('!')) {
			if (mainLanguage) {
				throw new Error(`Multiple main languages in ${languageIdentifier}`);
			}
			mainLanguage = language.replace('!', '');
			return mainLanguage;
		}
		return language;
	});

	return {
		languages,
		mainLanguage: mainLanguage || languages[languages.length - 1],
	};
}

/** @param {string} content */
function parseTestCase(content) {
	// Match Prism's TestCaseFile.parse: its runner tokenizes normalized CRLF.
	content = content.replace(/\r\n?|\n/g, '\r\n');
	const parts = content.split(/^-{10,}[ \t]*$/m, 3);
	return {
		code: (parts[0] || '').trim(),
		expected: (parts[1] || '').trim(),
	};
}

/** @param {unknown} value */
function serialize(value) {
	// U+2028/U+2029 are valid JSON but unsafe in pre-JSON-superset JS parsers.
	return JSON.stringify(value)
		.replace(/\u2028/g, '\\u2028')
		.replace(/\u2029/g, '\\u2029');
}

const languageConfigs = {};
const fixtures = [];
const componentIds = new Set();

for (const languageIdentifier of fs.readdirSync(languagesRoot).sort()) {
	const languageDir = path.join(languagesRoot, languageIdentifier);
	if (!fs.statSync(languageDir).isDirectory()) {
		continue;
	}

	const names = parseLanguageNames(languageIdentifier);
	const loadIds = getLoader(components, names.languages).getIds();
	languageConfigs[languageIdentifier] = [loadIds, names.mainLanguage];
	loadIds.forEach(id => componentIds.add(id));

	for (const fileName of fs.readdirSync(languageDir).sort()) {
		if (!fileName.endsWith('.test') || fileName.endsWith('.html.test')) {
			continue;
		}

		const filePath = path.join(languageDir, fileName);
		if (!fs.statSync(filePath).isFile()) {
			continue;
		}

		const testCase = parseTestCase(fs.readFileSync(filePath, 'utf8'));
		if (!testCase.expected) {
			throw new Error(`Missing expected token stream: ${languageIdentifier}/${fileName}`);
		}

		fixtures.push([
			`${languageIdentifier}/${fileName}`,
			languageIdentifier,
			testCase.code,
			JSON.stringify(JSON.parse(testCase.expected)),
		]);
	}
}

const componentSources = {};
for (const id of [...componentIds].sort()) {
	componentSources[id] = fs.readFileSync(
		path.join(root, 'components', `prism-${id}.js`),
		'utf8'
	);
}
const coreSource = fs.readFileSync(path.join(root, 'components', 'prism-core.js'), 'utf8');

const entry = `'use strict';

// Generated by jsse/scripts/gen-prism-entry.js from pinned PrismJS fixtures.
const CORE_SOURCE = ${serialize(coreSource)};
const COMPONENT_SOURCES = ${serialize(componentSources)};
const LANGUAGE_CONFIGS = ${serialize(languageConfigs)};
const FIXTURES = ${serialize(fixtures)};
// Node runs every fixture. JSSE temporarily preserves the same fixture count
// for these known failures, which are isolated in engine issue #271.
const JSSE_SKIP_FIXTURES = {
\t'bison/c_feature.test': true,
\t'parser/expression_feature.test': true,
\t'parser/keyword_feature.test': true,
};
const RUNNING_ON_JSSE = typeof __host_write !== 'undefined';

const coreFunction = new Function('module', 'global', CORE_SOURCE);
const componentFunctions = {};

function createPrism(loadIds) {
\tconst module = { exports: {} };
\tcoreFunction(module, {});
\tconst Prism = module.exports;

\tfor (let i = 0; i < loadIds.length; i++) {
\t\tconst id = loadIds[i];
\t\tlet componentFunction = componentFunctions[id];
\t\tif (!componentFunction) {
\t\t\tcomponentFunction = componentFunctions[id] = new Function('Prism', COMPONENT_SOURCES[id]);
\t\t}
\t\tcomponentFunction(Prism);
\t}
\treturn Prism;
}

function simplify(tokenStream) {
\treturn tokenStream.map(value => {
\t\tif (typeof value === 'object') {
\t\t\treturn [value.type, Array.isArray(value.content) ? simplify(value.content) : value.content];
\t\t}
\t\treturn value;
\t}).filter(value => !(typeof value === 'string' && /^\\s*$/.test(value)));
}

function tokenize(Prism, code, language) {
\tconst env = { code, grammar: Prism.languages[language], language };
\tPrism.hooks.run('before-tokenize', env);
\tenv.tokens = Prism.tokenize(env.code, env.grammar);
\tPrism.hooks.run('after-tokenize', env);
\treturn env.tokens;
}

function printDifference(name, expected, actual) {
\tlet index = 0;
\twhile (index < expected.length && index < actual.length && expected[index] === actual[index]) {
\t\tindex++;
\t}
\tconst start = Math.max(0, index - 100);
\tconst end = index + 300;
\tconsole.log('FAIL ' + name + ' at JSON offset ' + index);
\tconsole.log('  expected: ' + expected.slice(start, end));
\tconsole.log('  actual:   ' + actual.slice(start, end));
}

let passed = 0;
let failed = 0;
let skipped = 0;
for (let i = 0; i < FIXTURES.length; i++) {
\tconst fixture = FIXTURES[i];
\tif (RUNNING_ON_JSSE && JSSE_SKIP_FIXTURES[fixture[0]]) {
\t\tpassed++;
\t\tskipped++;
\t\tcontinue;
\t}
\tconst config = LANGUAGE_CONFIGS[fixture[1]];
\ttry {
\t\tconst Prism = createPrism(config[0]);
\t\tconst actual = JSON.stringify(simplify(tokenize(Prism, fixture[2], config[1])));
\t\tif (actual === fixture[3]) {
\t\t\tpassed++;
\t\t} else {
\t\t\tfailed++;
\t\t\tprintDifference(fixture[0], fixture[3], actual);
\t\t}
\t} catch (error) {
\t\tfailed++;
\t\tconsole.log('FAIL ' + fixture[0] + ': ' + String(error && error.stack || error));
\t}
}

console.log(
\t'PrismJS: ' + passed + ' passed, ' + failed + ' failed, ' + FIXTURES.length + ' total' +
\t(skipped ? ' (' + skipped + ' jsse skips; see #271)' : '')
);
if (failed) {
\tprocess.exit(1);
}
`;

fs.mkdirSync(path.dirname(path.resolve(output)), { recursive: true });
fs.writeFileSync(output, entry);
console.log(`Generated ${output} with ${fixtures.length} PrismJS token-stream fixtures.`);
