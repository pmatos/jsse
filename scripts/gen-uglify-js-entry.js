#!/usr/bin/env node
"use strict";

// Generate a filesystem-free entry for UglifyJS's compress DSL. Run from a
// pinned UglifyJS checkout; the sole argument is the output path.

const fs = require("fs");
const path = require("path");

const output = process.argv[2];
if (!output) {
  console.error("usage: gen-uglify-js-entry.js <output>");
  process.exit(2);
}

const root = process.cwd();
const runtimeFiles = [
  "lib/utils.js",
  "lib/ast.js",
  "lib/transform.js",
  "lib/parse.js",
  "lib/scope.js",
  "lib/compress.js",
  "lib/output.js",
  "lib/sourcemap.js",
  "lib/mozilla-ast.js",
  "lib/propmangle.js",
  "lib/minify.js",
  "test/exports.js",
];

function read(relative) {
  return fs.readFileSync(path.join(root, relative), "utf8");
}

function serialize(value) {
  // U+2028/U+2029 are valid JSON but unsafe in pre-JSON-superset JS parsers.
  return JSON.stringify(value)
    .replace(/\u2028/g, "\\u2028")
    .replace(/\u2029/g, "\\u2029");
}

const runtimeSource = runtimeFiles.map(read).join("\n\n");
const domprops = JSON.parse(read("tools/domprops.json"));
const fixtureRoot = path.join(root, "test", "compress");
const fixtureNames = fs
  .readdirSync(fixtureRoot)
  .filter((name) => name.endsWith(".js"))
  .sort();
const fixtures = fixtureNames.map((name) => ({
  name: "test/compress/" + name,
  source: fs.readFileSync(path.join(fixtureRoot, name), "utf8"),
}));

// Count through UglifyJS's own parser at prepare time. This locks the generated
// corpus to the same top-level labeled-case rule used by the runtime entry.
const U = require(path.join(root, "test", "node.js"));
let expectedCases = 0;
for (const fixture of fixtures) {
  const ast = U.parse(fixture.source, { filename: fixture.name, module: "" });
  let walker;
  walker = new U.TreeWalker((node) => {
    if (
      node instanceof U.AST_LabeledStatement &&
      walker.parent() instanceof U.AST_Toplevel
    ) {
      expectedCases++;
    }
  });
  ast.walk(walker);
}

const entry = `"use strict";

// Generated by jsse/scripts/gen-uglify-js-entry.js from pinned UglifyJS
// implementation sources and compress DSL fixtures.
var U = {};
new Function("domprops", "exports", ${serialize(runtimeSource)})(
    ${serialize(domprops)}, U);
var FIXTURES = ${serialize(fixtures)};
var EXPECTED_CASES = ${expectedCases};

function evaluate(code) {
    if (code instanceof U.AST_Node) code = make_code(code, { beautify: true });
    return new Function("return(" + code + ")")();
}

function make_code(ast, options, expression) {
    var stream = U.OutputStream(options);
    if (expression) ast = ast.clone(true).unwrap_expression();
    ast.print(stream);
    return stream.get();
}

function tmpl() {
    return U.string_template.apply(null, arguments);
}

function parse_tests(fixture) {
    var ast = U.parse(fixture.source, { filename: fixture.name, module: "" });
    var tests = Object.create(null);
    var tw = new U.TreeWalker(function(node) {
        if (node instanceof U.AST_LabeledStatement
            && tw.parent() instanceof U.AST_Toplevel) {
            var name = node.label.name;
            if (name in tests) {
                throw new Error('Duplicated test name "' + name + '" in ' + fixture.name);
            }
            tests[name] = get_one_test(name, node.body);
            return true;
        }
        if (!(node instanceof U.AST_Toplevel)) croak(node);
    });
    ast.walk(tw);
    return tests;

    function croak(node) {
        throw new Error(tmpl("Can't understand test file {file} [{line},{col}]\\n{code}", {
            file: fixture.name,
            line: node.start.line,
            col: node.start.col,
            code: make_code(node, { beautify: false }),
        }));
    }

    function read_string(stat) {
        if (stat.TYPE == "SimpleStatement") {
            var body = stat.body;
            switch (body.TYPE) {
              case "String":
                return body.value;
              case "Array":
                return body.elements.map(function(element) {
                    if (element.TYPE !== "String") {
                        throw new Error("Should be array of strings");
                    }
                    return element.value;
                }).join("\\n");
            }
        }
        throw new Error("Should be string or array of strings");
    }

    function get_one_test(name, block) {
        var test = { name: name, options: {} };
        var tw = new U.TreeWalker(function(node) {
            if (node instanceof U.AST_Assign) {
                if (!(node.left instanceof U.AST_SymbolRef)) croak(node);
                var name = node.left.name;
                if ([
                    "beautify",
                    "expression",
                    "mangle",
                    "options",
                    "rename",
                ].indexOf(name) < 0) {
                    croak(node);
                }
                test[name] = evaluate(node.right);
                return true;
            }
            if (node instanceof U.AST_LabeledStatement) {
                var label = node.label;
                if ([
                    "input",
                    "expect",
                    "expect_exact",
                    "expect_warnings",
                    "expect_stdout",
                    "node_version",
                ].indexOf(label.name) < 0) {
                    croak(node);
                }
                var stat = node.body;
                if (label.name == "expect_exact" || label.name == "node_version") {
                    test[label.name] = read_string(stat);
                } else if (label.name == "expect_stdout") {
                    // The parser/transform harness records this field but does
                    // not execute code through Node's vm/subprocess sandbox.
                    test[label.name] = true;
                } else {
                    test[label.name] = stat;
                }
                return true;
            }
        });
        block.walk(tw);
        return test;
    }
}

function to_toplevel(input, mangle_options, expression) {
    if (!(input instanceof U.AST_BlockStatement)) {
        throw new Error("Unsupported input syntax");
    }
    var offset = input.start.line;
    var tokens = [];
    input.walk(new U.TreeWalker(function(node) {
        if (U.push_uniq(tokens, node.start)) node.start.line -= offset;
        if (U.push_uniq(tokens, node.end)) node.end.line -= offset;
    }));
    var toplevel;
    if (!expression) {
        var directive = true;
        toplevel = new U.AST_Toplevel(input.transform(new U.TreeTransformer(function(node) {
            if (!directive) return node;
            if (node === input) return;
            if (node instanceof U.AST_SimpleStatement && node.body instanceof U.AST_String) {
                return new U.AST_Directive(node.body);
            }
            directive = false;
        })));
    } else if (input.body.length == 1) {
        toplevel = input.body[0].wrap_expression();
    } else {
        throw new Error("Invalid expression");
    }
    toplevel.figure_out_scope(mangle_options);
    return toplevel;
}

function test_case(test) {
    U.AST_Node.enable_validation();
    var output_options = test.beautify || {};
    var expect;
    if (test.expect) {
        expect = to_toplevel(test.expect, test.mangle, test.expression);
        expect = make_code(expect, output_options, test.expression);
    } else {
        expect = test.expect_exact;
    }
    var input = to_toplevel(test.input, test.mangle, test.expression);
    var input_code = make_code(input, {
        keep_quoted_props: true,
    }, test.expression);
    var input_formatted = make_code(test.input, {
        annotations: true,
        beautify: true,
        comments: "all",
        keep_quoted_props: true,
        quote_style: 3,
    });
    try {
        input.validate_ast();
        U.parse(input_code, { expression: test.expression });
    } catch (error) {
        console.log("FAIL cannot parse input:\\n" + input_formatted + "\\n" + String(error));
        return false;
    }
    var warnings_emitted = [];
    var expected_warnings;
    if (test.expect_warnings) {
        expected_warnings = make_code(test.expect_warnings, {
            beautify: false,
            quote_style: 2,
        });
        U.AST_Node.log_function(function(text) {
            warnings_emitted.push(text);
        }, /"INFO: /.test(expected_warnings));
    }
    var quoted_props;
    if (test.mangle && test.mangle.properties && test.mangle.properties.keep_quoted) {
        quoted_props = test.mangle.properties.reserved;
        if (!Array.isArray(quoted_props)) quoted_props = [];
        test.mangle.properties.reserved = quoted_props;
        U.reserve_quoted_keys(input, quoted_props);
    }
    if (test.rename) {
        input.figure_out_scope(test.mangle);
        input.expand_names(test.mangle);
    }
    var cmp = new U.Compressor(test.options, true);
    var output = cmp.compress(input);
    output.figure_out_scope(test.mangle);
    if (test.mangle) {
        output.compute_char_frequency(test.mangle);
        output.mangle_names(test.mangle);
        if (quoted_props) U.reserve_quoted_keys(input, quoted_props);
        if (test.mangle.properties) U.mangle_properties(output, test.mangle.properties);
    }
    var output_code = make_code(output, output_options, test.expression);
    U.AST_Node.log_function();
    if (expect != output_code) {
        console.log([
            "FAIL output mismatch",
            "---INPUT---",
            input_formatted,
            "---OUTPUT---",
            output_code,
            "---EXPECTED---",
            expect,
        ].join("\\n"));
        return false;
    }
    try {
        output.validate_ast();
        U.parse(output_code, { expression: test.expression });
    } catch (error) {
        console.log("FAIL cannot reparse output:\\n" + output_code + "\\n" + String(error));
        return false;
    }
    if (test.expect_warnings) {
        var actual_warnings = JSON.stringify(warnings_emitted);
        if (expected_warnings != actual_warnings) {
            console.log([
                "FAIL warning mismatch",
                "---EXPECTED WARNINGS---",
                expected_warnings,
                "---ACTUAL WARNINGS---",
                actual_warnings,
            ].join("\\n"));
            return false;
        }
    }
    return true;
}

var passed = 0;
var failed = 0;
var total = 0;
for (var fixture_index = 0; fixture_index < FIXTURES.length; fixture_index++) {
    var fixture = FIXTURES[fixture_index];
    var tests;
    try {
        tests = parse_tests(fixture);
    } catch (error) {
        console.log("FAIL " + fixture.name + ": " + String(error && error.stack || error));
        failed++;
        continue;
    }
    var names = Object.keys(tests);
    total += names.length;
    for (var test_index = 0; test_index < names.length; test_index++) {
        var name = names[test_index];
        try {
            if (test_case(tests[name])) {
                passed++;
            } else {
                failed++;
                console.log("  case: " + fixture.name + " / " + name);
            }
        } catch (error) {
            failed++;
            console.log("FAIL " + fixture.name + " / " + name + ": "
                + String(error && error.stack || error));
        }
    }
}
if (total != EXPECTED_CASES) {
    failed++;
    console.log("FAIL discovered " + total + " cases, expected " + EXPECTED_CASES);
}
console.log("UglifyJS compress: " + passed + " passed, " + failed + " failed, "
    + EXPECTED_CASES + " total");
if (failed) process.exit(1);
`;

fs.mkdirSync(path.dirname(path.resolve(output)), { recursive: true });
fs.writeFileSync(output, entry);
console.log(
  `Generated ${output} with ${fixtures.length} fixture files and ${expectedCases} cases.`
);
